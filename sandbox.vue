        state.user = val;
    },
    updateLoggedInLink(state, val) {
        state.loggedInLink = val;
    },
    toggle(state, bool) {
      state._isTrue = bool;
    },
    gotFBData(state, val) {
      state.loadedFBData = val
    }
  },
  
  actions: {
    async updateFBData({commit}) {
      commit('gotFBData', await getFBData())
      commit('toggle', await getFBData())
    }
  }
