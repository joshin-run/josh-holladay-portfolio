  state: {
    user: '',
    loggedInLink: 'Login',
    loggedIn: false,
    _isTrue: null,
    loadedFBData: null
  },
  
  getters: {
    // if user is logged in...change loggedIn to true...
    // if user is logged in...change loggedInLink to 'Logout'
